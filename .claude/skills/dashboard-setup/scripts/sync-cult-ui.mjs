import { execFileSync } from "node:child_process"
import { existsSync, mkdirSync, rmSync, copyFileSync, writeFileSync } from "node:fs"
import { dirname, join, relative } from "node:path"
import { fileURLToPath } from "node:url"

const SOURCE_REPO = "https://github.com/nolly-studio/cult-ui.git"
const SOURCE_COMMIT = "a3308bad8496b036adf2fbd29d50b877fb3c5987"

const __filename = fileURLToPath(import.meta.url)
const skillDir = dirname(dirname(__filename))
const cacheDir = join(skillDir, ".cache")
const usingExternalSource = Boolean(process.env.CULT_UI_SOURCE)
const repoDir = process.env.CULT_UI_SOURCE || join(cacheDir, "cult-ui")
const vendorDir = join(skillDir, "vendor", "cult-ui-selected")

