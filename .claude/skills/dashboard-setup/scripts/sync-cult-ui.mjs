function run(command, args, cwd) {
  execFileSync(command, args, { cwd, stdio: "inherit" })
}

function ensureRepo() {
  if (!usingExternalSource) {
    mkdirSync(cacheDir, { recursive: true })
  }

  if (!existsSync(repoDir)) {
    run("git", ["clone", SOURCE_REPO, repoDir], skillDir)
  }

  run("git", ["fetch", "--depth", "1", "origin", SOURCE_COMMIT], repoDir)
  run("git", ["checkout", "--detach", SOURCE_COMMIT], repoDir)
}

function copySelectedFiles() {
  rmSync(vendorDir, { recursive: true, force: true })
  mkdirSync(vendorDir, { recursive: true })

  const copied = []
  const missing = []

  for (const file of selectedFiles) {
    const source = join(repoDir, file)
    const destination = join(vendorDir, file)

    if (!existsSync(source)) {
      missing.push(file)
      continue
    }

    mkdirSync(dirname(destination), { recursive: true })
    copyFileSync(source, destination)
    copied.push(file)
  }

  const manifest = {
    sourceRepo: SOURCE_REPO,
    sourceCommit: SOURCE_COMMIT,
    generatedAt: new Date().toISOString(),
    copied,
    missing,
    notes: [
      "Cult UI is MIT-licensed at the pinned source commit.",
      "This package copies a selected dashboard-relevant subset only.",
      "Do not assume offsite/pro links in the README are included in this license bundle.",
    ],
  }

  writeFileSync(
    join(vendorDir, "MANIFEST.generated.json"),
    `${JSON.stringify(manifest, null, 2)}\n`
  )

  writeFileSync(
    join(vendorDir, "SOURCE.md"),
    [
      "# Cult UI Selected Vendor Bundle",
      "",
      `Source repo: ${SOURCE_REPO}`,
      `Pinned commit: ${SOURCE_COMMIT}`,
      "",
      "This folder is generated by `scripts/sync-cult-ui.mjs`.",
      "Keep `LICENSE.md` with any redistributed or adapted source.",
      "",
      "Copied files:",
      ...copied.map((file) => `- ${file}`),
      "",
      missing.length ? "Missing files:" : "Missing files: none",
      ...missing.map((file) => `- ${file}`),
      "",
    ].join("\n")
  )

  console.log(`Copied ${copied.length} files to ${relative(process.cwd(), vendorDir)}`)

  if (missing.length) {
    console.error(`Missing ${missing.length} files:`)
    for (const file of missing) console.error(`- ${file}`)
    process.exitCode = 1
  }
}

ensureRepo()
copySelectedFiles()
